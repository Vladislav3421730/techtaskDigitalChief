CREATE TABLE products (
    id SERIAL PRIMARY KEY,
    name VARCHAR(100),
    description TEXT,
    is_active BOOLEAN,
    created_at DATE,
    last_update TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

CREATE TABLE sku (
    id SERIAL PRIMARY KEY,
    product_id INTEGER REFERENCES products(id),
    color VARCHAR(50),
    size ENUM('S', 'M', 'L', 'XL') NOT NULL,
	cost NUMERIC(10, 2)
);
